#include "app.hpp"

using namespace Dino;

App *App::app = nullptr;

App::App()
{
    this->startupLogo();
    std::cout << "App Init" << std::endl;
}

App *App::getInstance()
{
    if (!app)
    {
        app = new App();
    }
    return app;
}

Mongoose::Server *App::getServerInstance()
{
    if (this->server)
    {
        return this->server;
    }
    else
    {
        std::invalid_argument("Server uninitialized");
        return nullptr;
    }
}

